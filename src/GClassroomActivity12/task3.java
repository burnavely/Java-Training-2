package GClassroomActivity12;


public class task3 {

	public static void main(String[] args) {
		int n1 = 25, n2 = 18;
		int sum, prod, diff;
		double nn1 = 25.0, nn2 = 18.0;
		double quo;
		
		sum = n1+n2;
		prod = n1*n2;
		diff = n1-n2;
		quo = nn1/nn2;
		
		System.out.println("Sum of " + n1 + " and " + n2 + " is " + sum);
		System.out.println("Product of " + n1 + " and " + n2 + " is " + prod);
		System.out.println("Difference of " + n1 + " and " + n2 + " is " + diff);
		System.out.println("Quotient of " + n1 + " and " + n2 + " is " + quo);

	}

}
